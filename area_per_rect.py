# This program calculates the area & perimeter of 6cm  X 7cm rectangle


def main():
    print("For a rectangle with a length 6cm and width 7cm:")
    print("The area is : {}cm^2".format(6 * 7))
    print("The perimeter is: {}cm".format(2 * (6 + 7)))


if __name__ == "__main__":
    main()
